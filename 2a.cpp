#include <bits/stdc++.h>
using namespace std;

int main()
{
	//use of operators
	int a = 5;
	a = a + 1;
	cout << "line 9 out =  "<< a << endl;
	cout << "line 10 out = "<< a++ << endl;
	cout << "line 11 out = "<< a << endl;
	a++;
	cout << "line 13 out = "<< a << endl;
	cout << "line 14 out = "<< ++a << endl;


}